const DAY_CONFIG = {
  5: { tips: FRIDAY_TIPS,   label: "Пятница",      icon: "🍷", accent: C.purple },
  6: { tips: SATURDAY_TIPS, label: "Суббота",      icon: "🌿", accent: C.orange },
  0: { tips: SUNDAY_TIPS,   label: "Воскресенье",  icon: "📋", accent: C.blue   },
};

export function WeekendTipsBar({ weekNum }) {
  const weekday = new Date().getDay();
  const cfg = DAY_CONFIG[weekday];
  if (!cfg) return null;

  const tip = cfg.tips[((weekNum || 1) - 1) % cfg.tips.length];

  return (
    <div style={{
      background: `${cfg.accent}14`,
      border: `1px solid ${cfg.accent}44`,
      borderRadius: 14,
      padding: "12px 14px",
      marginBottom: 14,
      display: "flex",
      alignItems: "flex-start",
      gap: 12,
    }}>
      <div style={{
        width: 32, height: 32, flexShrink: 0,
        borderRadius: 10,
        background: `${cfg.accent}26`,
        display: "flex", alignItems: "center", justifyContent: "center",
        fontSize: 18,
      }}>{cfg.icon}</div>
      <div style={{ flex: 1, minWidth: 0 }}>
        <div style={{
          fontSize: 10,
          color: cfg.accent,
          fontWeight: 600,
          textTransform: "uppercase",
          letterSpacing: "0.12em",
          marginBottom: 4,
          fontFamily: F.sans,
        }}>
          {cfg.label} · подсказка
        </div>
        <div style={{
          fontSize: 13,
          color: C.text,
          lineHeight: 1.5,
          fontFamily: F.sans,
        }}>
          {tip}
        </div>
      </div>
    </div>
  );
}
